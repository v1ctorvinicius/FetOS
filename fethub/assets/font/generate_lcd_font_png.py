# preenche faltantes com '?'
for c in range(32, 128):
    if c not in FONT_5x8:
        FONT_5x8[c] = FONT_5x8[ord('?')]

def main():
    W = 5 * 16  # 80
    H = 8 * 6   # 48

    img = Image.new("1", (W, H), 0)

    for idx, code in enumerate(range(32, 128)):
        row = idx // 16
        col = idx % 16
        x0 = col * 5
        y0 = row * 8

        glyph = FONT_5x8[code]

        for y in range(8):
            bits = glyph[y]
            for x in range(5):
                bit = (bits >> (4 - x)) & 1
                img.putpixel((x + x0, y + y0), 255 if bit else 0)

    out_path = os.path.join(os.path.dirname(__file__), "lcd_5x8.png")
    img.save(out_path)
    print("LCD font generated:", out_path)

if __name__ == "__main__":
    main()
